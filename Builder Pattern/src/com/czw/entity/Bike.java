package com.czw.entity;

public class Bike {
    private Iframe frame;
    private Iseat seat;
    private Itire tire;

    public Iframe getFrame() {
        return frame;
    }

    public void setFrame(Iframe frame) {
        this.frame = frame;
    }

    public Iseat getSeat() {
        return seat;
    }

    public void setSeat(Iseat seat) {
        this.seat = seat;
    }

    public Itire getTire() {
        return tire;
    }

    public void setTire(Itire tire) {
        this.tire = tire;
    }
}
